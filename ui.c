{
    memset(buf, 0x0, 200);

    if(index < 4) {
        sprintf(buf, "%s|%s",
            common_macropad_cmd[index].tag,
            common_macropad_cmd[index].cmd);
    } else {
        sprintf(buf, "%s|%s",
        user_macropad_cmd[menu_index][index-4].tag,
        user_macropad_cmd[menu_index][index-4].cmd);
    }
}

